function toggleJava() {
  var javaDiv = document.querySelector(".topJava");
  javaDiv.style.display =
    javaDiv.style.display === "none" || javaDiv.style.display === ""
      ? "block"
      : "none";

  var setaImg = document.querySelector(".seta");
  setaImg.classList.toggle("virada");
}

